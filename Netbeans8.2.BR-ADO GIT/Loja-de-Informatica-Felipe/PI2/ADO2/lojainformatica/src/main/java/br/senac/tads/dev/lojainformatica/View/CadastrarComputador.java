    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        boolean preenchido = ValidarFormulario();
        if(preenchido){
            if(objComputador!=null){

            int id = Integer.parseInt(this.lblID.getText());
            objComputador.setProcessador(txtProcessador.getText());
            objComputador.setHd(txtHd.getText());
            

            if(ComputadorDAO.atualizar(objComputador)){
                JOptionPane.showMessageDialog(this, "Alteração realizada com sucesso!");
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "Problema ao realizar alteração!");
            }

            limparFormulario();

        }else{
            Computador objComputador2 = new Computador();

            objComputador2.setProcessador(txtProcessador.getText());
            objComputador2.setHd(txtHd.getText());

            if(ComputadorDAO.salvar(objComputador2)){
                JOptionPane.showMessageDialog(this, "Inclusão realizada com sucesso!");

            }else{
                JOptionPane.showMessageDialog(this, "Erro ao realizar a inclusão");
            }
        }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

