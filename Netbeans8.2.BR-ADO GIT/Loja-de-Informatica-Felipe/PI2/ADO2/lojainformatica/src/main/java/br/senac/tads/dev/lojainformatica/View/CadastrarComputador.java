        private void preencherFormulario(int ID) {
        objComputador = ComputadorDAO.consultarComputador(ID);
        
        if(objComputador!=null){
            this.lblID.setText(String.valueOf(objComputador.getIdComputador()));
            this.txtProcessador.setText(objComputador.getProcessador());
            this.txtHd.setText(objComputador.getHd());
        }
        
    }

    private void limparFormulario() {
        this.lblID.setText("");
        this.txtProcessador.setText("");
        this.txtHd.setText("");
         
        objComputador = null;
    }
    
     private boolean ValidarFormulario() {

        if (this.txtProcessador.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Defina um processador para o computador!");
            return false;
        }
        if (this.txtHd.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Defina o hd para o computador!");
            return false;
        }
        return true;

    }

