    private boolean ValidarFormulario() {

        if (this.txtDataInicio.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Defina uma data inicio!");
            return false;
        }
         if (this.txtDatafim.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Defina uma data fim!");
            return false;
        }
        return true;
    }
