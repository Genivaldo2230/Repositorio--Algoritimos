    private void txtDataInicioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDataInicioKeyTyped
       char c = evt.getKeyChar();
       
       if(c != '/'){
           if ( ((c < '0') || (c > '9')) && (c != KeyEvent.VK_BACKSPACE)    ) {
            evt.consume();
        }
       }
            
        if(txtDataInicio.getText().length() > 9){
             evt.consume();
            JOptionPane.showMessageDialog(this, "atingiu o limte");
        }
    }//GEN-LAST:event_txtDataInicioKeyTyped

